import random

def dado(): #Calcula dos numeros obtenidos por un dado
  resultado_1 = int(random.randint(1,6))
  print(f"Número 1: {resultado_1}")
  resultado_2 = int(random.randint(1,6))
  print(f"Número 2: {resultado_2}")
  Producto = resultado_1 * resultado_2
  return Producto

def verificar_multiplo():#Verifica si es multiplo de 10, 5, 2 o ninguno de estos
  numero = dado()
  if numero % 10 == 0:#Usamos 10 por que los multiplos de 10 son los que te hacen perder y simplifica las cosas
    return False
  elif numero % 2 == 0:
    return True
  elif numero % 5 == 0:
    return True
  else:
    return False

