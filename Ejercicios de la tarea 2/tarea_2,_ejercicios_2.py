# Estilos de nodos
dot.attr('node', shape='ellipse', style='filled', color='#D5E8D4')
dot.node('start', 'Inicio')
dot.node('end', 'Fin')

dot.attr('node', shape='diamond', style='filled', color='#FFF2CC')
dot.node('menu', '¿Menú: Jugar (1)?')
dot.node('listo', '¿Listo? (S/N)')
dot.node('mod10', '¿Producto es\nmúltiplo de 10?') # Primer filtro
dot.node('mod25', '¿Producto es\nmúltiplo de 2 o 5?') # Segundo filtro
dot.node('ver_empate', '¿Jugador y Compu\nson True?') # Condición de empate

dot.attr('node', shape='box', style='filled', color='#F5F5F5')
dot.node('lan_dados', 'Función dado()\n(Lanzar 2 dados,\ncalcular producto)')
dot.node('res_multiplo', 'verificar_multiplo()\nRetorna True/False')
dot.node('lan_desempate', 'Función dado_2()\n(Desempate, gana el mayor)')

dot.attr('node', shape='parallelogram', style='filled', color='#DAE8FC')
dot.node('input_m', 'Leer Opción Menú')
dot.node('print_win', 'Mostrar Ganador\no "Nadie Gana"')

# Conexiones
dot.edge('start', 'input_m')
dot.edge('input_m', 'menu')
dot.edge('menu', 'end', label='2')
dot.edge('menu', 'listo', label='1')

dot.edge('listo', 'input_m', label='N')
dot.edge('listo', 'lan_dados', label='S')

